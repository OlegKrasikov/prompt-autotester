'use client';

import React from 'react';
import { Button } from './Button';

export interface EmptyStateProps {
  icon?: React.ReactNode;
  title: string;
  description?: string;
  action?: {
    label: string;
    onClick: () => void;
    variant?: 'primary' | 'secondary' | 'danger' | 'ghost';
  };
  className?: string;
}

const EmptyState: React.FC<EmptyStateProps> = ({ icon, title, description, action, className }) => {
  return (
    <div
      className={`flex flex-col items-center justify-center px-4 py-12 text-center ${className || ''}`}
    >
      {icon && <div className="mb-4 text-[color:var(--color-muted-foreground)]">{icon}</div>}
      <h3 className="mb-2 text-lg font-semibold text-[color:var(--color-foreground)]">{title}</h3>
      {description && (
        <p className="mb-6 max-w-sm text-sm text-[color:var(--color-muted-foreground)]">
          {description}
        </p>
      )}
      {action && (
        <Button variant={action.variant || 'primary'} onClick={action.onClick}>
          {action.label}
        </Button>
      )}
    </div>
  );
};

